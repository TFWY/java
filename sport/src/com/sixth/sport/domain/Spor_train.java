package com.sixth.sport.domain;

public class Spor_train {
	public int id;
	public String trainnum;
	public String trainname;
	public String trainteacher;
	public String trainSchool;
	public String trainaddress;
	public String traintime;
	public String remark;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getTrainnum() {
		return trainnum;
	}
	public void setTrainnum(String trainnum) {
		this.trainnum = trainnum;
	}
	public String getTrainname() {
		return trainname;
	}
	public void setTrainname(String trainname) {
		this.trainname = trainname;
	}
	public String getTrainteacher() {
		return trainteacher;
	}
	public void setTrainteacher(String trainteacher) {
		this.trainteacher = trainteacher;
	}
	public String getTrainSchool() {
		return trainSchool;
	}
	public void setTrainSchool(String trainSchool) {
		this.trainSchool = trainSchool;
	}
	public String getTrainaddress() {
		return trainaddress;
	}
	public void setTrainaddress(String trainaddress) {
		this.trainaddress = trainaddress;
	}
	public String getTraintime() {
		return traintime;
	}
	public void setTraintime(String traintime) {
		this.traintime = traintime;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	

}
