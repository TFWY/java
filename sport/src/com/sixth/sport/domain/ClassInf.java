package com.sixth.sport.domain;

public class ClassInf {

	private int id;
	private String className;
	private String classtime;
	private String classcommon;
	private String classremark;
	private String classId;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getClasstime() {
		return classtime;
	}
	public void setClasstime(String classtime) {
		this.classtime = classtime;
	}
	public String getClasscommon() {
		return classcommon;
	}
	public void setClasscommon(String classcommon) {
		this.classcommon = classcommon;
	}
	public String getClassremark() {
		return classremark;
	}
	public void setClassremark(String classremark) {
		this.classremark = classremark;
	}
	public String getClassId() {
		return classId;
	}
	public void setClassId(String classId) {
		this.classId = classId;
	}

	
	
}
